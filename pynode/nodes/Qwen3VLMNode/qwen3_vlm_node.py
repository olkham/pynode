"""
Qwen3-VL Vision Language Model node.
Performs vision-language inference on images using the Qwen3-VL model.

Supports two modes:
  - Generic: Answer any text prompt about an image.
  - Detection: Locate objects and return structured detections compatible with
    downstream YOLO-style nodes (requires the `supervision` package).
"""

import logging
from typing import Any, Dict, List, Optional

import numpy as np

from pynode.nodes.base_node import BaseNode, Info, MessageKeys

logger = logging.getLogger(__name__)

