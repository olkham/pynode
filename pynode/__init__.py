def _resolve_version() -> str:
    """Best-effort package version.

    Prefers the installed distribution metadata (the authoritative version for
    a `pip install`), then the file setuptools_scm writes at build time, and
    finally a placeholder for a bare source checkout that was never built.
    """
    try:
        from importlib.metadata import PackageNotFoundError, version
        try:
            v = version("pynode-flow")
            # 0.0.0 is the placeholder used during Docker builds; skip it so
            # the real version written to _version.py is preferred.
            if v != "0.0.0":
                return v
        except PackageNotFoundError:
            pass
    except Exception:
        pass
    try:
        from pynode._version import __version__ as _scm_version  # generated by setuptools_scm
        return _scm_version
    except Exception:
        return "0.0.0+unknown"


__version__ = _resolve_version()
__all__ = ['WorkflowEngine', '__version__']
